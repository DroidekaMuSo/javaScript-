let number = parseInt(prompt("Dame un numero"));
switch (number) {
  case 1:
    alert("Elegiste a Homero");
    break;
  case 2:
    alert("Elegiste a Marge");
    break;
  case 3:
    alert("Elegiste a Bart");
    break;
  case 4:
    alert("Elegiste a Lisa");
    break;

  default:
    alert("Dame un numero del 1 al 4");
    break;
}
